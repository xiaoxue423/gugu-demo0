<template>
  <div class="layout" :class="layoutClass">
    <slot></slot>
  </div>
</template>

<script>
export default {
  name: "layout.vue",
  data(){
    return {
      layoutClass:{
        hasSider:false
      }
    }
  },
  mounted() {
    this.$children.forEach((vm)=>{
      console.log(vm.$options.name)
      if(vm.$options.name === 'GuguSider'){
        this.layoutClass.hasSider = true
      }
    })
  }
}
</script>

<style scoped lang="scss">
.layout {
  flex-grow: 1;
  display: flex;
  flex-direction: column;
  border: 1px solid red;
  &.hasSider{
    flex-direction: row;
  }
}
</style>
